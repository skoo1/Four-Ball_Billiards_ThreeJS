"""
4-ball billiards script example — 기본 샷 예시
===============================================

사용법:
    게임 창에서  Shift+S  를 누르거나,
    Python 코드에서:
        from main import load_script_file
        load_script_file("scripts/example_shot.py")

    또는 main.py 하단 app.run() 앞에서 직접 호출:
        execute_script(SCRIPT)

좌표계 참고:
    TABLE_WIDTH  = 1.27 m   (-0.635 ≤ x ≤  0.635)  ← 좌우
    TABLE_LENGTH = 2.54 m   (-1.27  ≤ z ≤  1.27 )  ← 원/근
    원점 = 테이블 중앙.  +Z = 원쿠션 방향, +X = 오른쪽

azimuth 참고:
      0° → +Z (원쿠션 방향)
     90° → +X (오른쪽 쿠션)
    180° → -Z (근쿠션 방향)
    270° → -X (왼쪽 쿠션)
"""

SCRIPT = {
    # ── 공 배치 ──────────────────────────────────────────────────────────────
    "setup": {
        "white":  ( 0.0, -0.5),   # 흰공 (수구)
        "yellow": ( 0.3,  0.3),   # 노란공 (상대 수구)
        "red1":   (-0.2,  0.6),   # 빨간공 1 (적구)
        "red2":   ( 0.2,  0.8),   # 빨간공 2 (적구)
    },

    # ── 큐 샷 파라미터 ───────────────────────────────────────────────────────
    "shot": {
        "ball":      "white",  # 어떤 공을 칠지 (white / yellow / red1 / red2)

        # 방향 ──────────────────────────────────────────────────────────────
        "azimuth":   5.0,      # 수평 방향각 (도).  0 = +Z, 90 = +X, 시계방향 +
        "elevation": 0.0,      # 수직 경사각 (도).  0 = 수평, 45 = 아래쪽 (masse)

        # 당점 (팁 오프셋) ───────────────────────────────────────────────────
        # ball_radius 기준 정규화 값.  범위: -1.0 ~ +1.0
        "tip_x":     0.0,      # 좌우 English:  -1=왼쪽,  0=중심,  +1=오른쪽
        "tip_y":     0.15,     # 상하 English:  -1=끌기,  0=중심,  +1=밀기

        # 세기 ───────────────────────────────────────────────────────────────
        "force":     1.1,      # 충격량 (N·s 상당).  일반 범위 0.3 ~ 1.5

        # 스트로크 애니메이션 (물리에는 영향 없음) ─────────────────────────
        "stroke_profile": {
            "aim_delay":      0.8,   # 초: 큐가 조준 위치에서 머무는 시간
            "backswing_dist": 0.12,  # m:  백스윙 거리
            "backswing_time": 0.4,   # 초: 백스윙 소요 시간
            "strike_time":    0.15,  # 초: 전진 타격 소요 시간
        },
    },
}
